CREATE TABLE IF NOT EXISTS product (
	id  INT NOT NULL AUTO_INCREMENT,
	brandid VARCHAR(100),
	productid VARCHAR(100),
	startdate VARCHAR(100),
	enddate VARCHAR(100),
	pricelist VARCHAR(100),
	priority VARCHAR(100),
	price VARCHAR(100),
	currency VARCHAR(100),
	currentamount VARCHAR(100),
	PRIMARY KEY (id)
);


